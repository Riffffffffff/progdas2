umur = int (input("masukan umur"))
if umur <=2 :
    panggilan = bayi
elif umur >2 ^ umur <=5 :
    panggilan = balita
elif umur >5 ^ umur <=12 :
    panggilan = anak-anak
elif umur >12 ^ umur <=17 :
    panggilan = remaja
elif umur >27 :
    panggilan = dewasa
elif umur >30 :
    panggilan = orangtua
    